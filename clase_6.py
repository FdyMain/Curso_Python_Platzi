#Operadores numéricos
a = 10
b = 3

print("Suma:", a + b)
print("Resta:", a - b)
print("Multiplicación:", a * b)
print("División:", a / b)

print("Módulo:", a % b)

#Si el resultado es decimal, solo muestra el entero, elimina los decimales
print("Parte entera de la División:", a // b)

#Elevado al cubo
print("Potenciación:", a ** b)

#Shorcuts
a += 2
print(a)

a *= 2
print(a)

